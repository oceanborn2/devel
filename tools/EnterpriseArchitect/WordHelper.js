function WordHelper() 
{
	this.begindocument = function (outname) {
		Session.Output("WordHelper::begindocument()");
		// Dmarrage de l'API Automation 
		this.outname = outname;
		this.wordObj = new ActiveXObject("Word.Application"); 
		this.doc = this.wordObj.Documents.Add();
		this.doc.Activate();
		return this.doc;
	};
	
	this.enddocument = function() {
		Session.Output("WordHelper::enddocument()");
		this.doc.SaveAs(this.outname);	
	};
	
	this.cleanup = function() {
		Session.Output("WordHelper::cleanup()");
		this.wordObj.Quit();
	};
	
	this.title = function (texte, niveau) {
		//Session.Output("WordHelper::titre()");
		style = 'Titre ' + niveau.toString(); // valeur numrique
		with (this.wordObj) {
			Selection.TypeText(texte);		
			Selection.Style = ActiveDocument.Styles(style);
			Selection.TypeParagraph();
			//Selection.HomeKey(0);
			/*with (Selection.HomeKey) {
				Unit = 0;
				Extend = 1;
			}*/
//			var p = ActiveDocument.Content.Paragraphs.Add();
//			this.doc.Content.InsertParagraphAfter(p);
		}
//		this.doc.Paragraphs.Add();
	};
	

	this.normal = function (texte) {
		//Session.Output("WordHelper::normal()");
		this.wordObj.Selection.TypeText(texte);
		this.wordObj.Selection.TypeParagraph();
		//var p = this.doc.Content.Paragraphs.Add();
		////this.doc.InsertParagraphAfter(p);
	};

	this.frametable = function (texte) {
		//Session.Output("WordHelper::frametable()");
		this.normal(texte);
	};
		
	this.paste = function() {
//		this.wordObj.InsertParagraphAfter();
		this.wordObj.Selection.Paste();
	};
	
}
